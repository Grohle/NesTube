_current_lang: str = "en"


def set_language(lang: str) -> None:
    global _current_lang
    if lang in _TRANSLATIONS:
        _current_lang = lang


def get_language() -> str:
    return _current_lang


def t(key: str, **kwargs) -> str:
    """Get translated string. Supports format kwargs."""
    text = _TRANSLATIONS.get(_current_lang, _TRANSLATIONS["es"]).get(key, key)
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, IndexError):
            pass
    return text


def default_cut_name(n: int) -> str:
    """Localized default label for a new cut row."""
    return t("default_cut_name", n=n)


def localize_cut_description(desc: str, n: int) -> str:
    """Re-localize auto-generated cut labels when the UI language changes."""
    if not desc:
        return default_cut_name(n)
    legacy = f"Corte {n}"
    for strings in _TRANSLATIONS.values():
        pattern = strings.get("default_cut_name")
        if not pattern:
            continue
        try:
            if desc == pattern.format(n=n):
                return default_cut_name(n)
        except (KeyError, IndexError):
            continue
    if desc == legacy:
        return default_cut_name(n)
    return desc


def available_languages() -> list:
    return list(_TRANSLATIONS.keys())
